package ex_27_09_2024;

public class SumofNumbers {
    public static void main(String[] args) {
        int num=100;
        int sum=0;
        int i=1;
        while(i<=num)
        {
            sum=sum+i;
            i++;
        }
        System.out.println(" sum of numbers till 100 is " +sum);

    }
}
